function send_photo() {

  /* Забираем отправленные данные */
  $phone_name = $_POST['phone_name'];
  $phone = $_POST['phone'];

  /* Отправляем нам письмо */
  $emailTo = 'metalldemontag2019@gmail.com';
  $subject = 'Оценка по фото';
  $headers = "Content-type: text/html; charset=\"utf-8\"";
  $mailBody = "Имя $phone_name . Телефон $phone";  

  $attachments = array();
  for ($i = 0; $i < count($_FILES); $i++) {
    $uploadedfile = $_FILES[$i];
    $movefile = wp_handle_upload( $uploadedfile, array( 'test_form' => false ) );
    array_push($attachments, $movefile['file']);
  }

  wp_mail($emailTo, $subject, $mailBody, $headers, $attachments);

  foreach($attachments as $attachment) {
    unlink( $attachment );
  }
  /* Завершаем выполнение ajax */
  die();  
}

add_action("wp_ajax_send_photo", "send_photo");
